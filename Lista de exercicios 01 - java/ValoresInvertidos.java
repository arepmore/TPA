
public class ValoresInvertidos {
	public static void main (String args []) {
		
		int valorA, valorB, valorC;
		
		valorA=50;
		valorB=80;
		
		valorC=valorA;
		valorA=valorB;
		valorB=valorC;
		
		System.out.println ("Os valores invertidos, ficam respecitivamente. 1° valor =" +valorA+  
				" 2° valor=" +valorB);
		
	}
}
